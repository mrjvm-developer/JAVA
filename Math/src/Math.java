
public class Math {

	public static void main(String[] args) {
		
		int myInt = 3;
		double myDouble = -5.5;
		
		System.out.println(3);
		System.out.println(3+4);
		System.out.println(3-4);
		System.out.println(3/4);
		System.out.println(3*4);
		System.out.println(3.0/4.0);
		System.out.println(10%3);
		
		System.out.println(5 + myInt);
		System.out.println(myInt + myDouble);
		System.out.println(java.lang.Math.abs(myDouble));
		System.out.println(java.lang.Math.pow(myInt, 3));
		System.out.println(java.lang.Math.sqrt(36));
		System.out.println(java.lang.Math.min(2, 6));
		System.out.println(java.lang.Math.max(2, 6));
		System.out.println(java.lang.Math.round(myDouble));
		System.out.println(java.lang.Math.random());
	}
}